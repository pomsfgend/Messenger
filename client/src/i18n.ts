export const translateApiError = (message: string, t: (key: string) => string): string => {
    switch (message) {
        case 'Invalid username or password':
            return t('login.invalidCredentials');
        case 'This name is taken by a registered user.':
            return t('login.guestNameTaken');
        default:
            return message;
    }
};