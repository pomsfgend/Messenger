// API Error Translation Map
const apiErrorTranslations: Record<string, keyof typeof translations.ru> = {
    'Server error during guest login.': 'toast.guestLoginError',
    'A registered user with this username already exists.': 'register.usernameExists',
    'This name is used by a registered user. Please choose another name to enter as a guest.': 'login.guestNameTaken',
    'Google Sign-In is not configured on the server.': 'toast.googleConfigError',
    'Telegram login is not configured.': 'toast.telegramConfigError'
};

/**
 * Translates a known API error message. If the message is not in the map,
 * it returns the original message.
 * @param originalMessage The error message from the API (in English).
 * @param t The translation function from useI18n.
 * @returns The translated string or the original message.
 */
export const translateApiError = (originalMessage: string, t: (key: any) => string): string => {
    const key = apiErrorTranslations[originalMessage];
    return key ? t(key) : originalMessage;
};


